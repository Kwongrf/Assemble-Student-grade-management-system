SORTID	PROC NEAR
		PUSH AX
		PUSH BX
		PUSH CX
		PUSH DX
		PUSH SI
		PUSH DI
		MOV CX, NUMBER
		PUSH CX; protect CX
		LEA BX, SORTED
		XOR SI, SI
		XOR DX, DX
LP0:
		MOV [BX+SI], DX
		INC DX
		ADD SI,2
		LOOP LP0

		POP CX
		DEC CX
		
LP1:
		MOV  DI, CX
		LEA BX, ID_ARR
		LEA SI, SORTED
LP2:
		MOV AX, [BX]
		CMP AX, [BX+2]
		JLE CONTINUE
		MOV DX, [SI]
		XCHG DX, [SI+2]
		MOV [SI], DX
CONTINUE:
		ADD BX, 2
		ADD SI, 2
		LOOP LP2
		MOV CX, DI
		LOOP LP1

		POP DI
		POP SI
		POP DX
		POP CX
		POP BX
		POP AX
		RET
SORTID 	ENDP
