DECOUT PROC NEAR
		PUSH AX
		PUSH BX
		PUSH CX
		PUSH DX
		MOV BX, BUFREAR
		CMP AX, 0
		JZ ZERO
OUTLOOP:
		XOR AH, AH
		OR  AL, AL; number is in AX
		JZ	OUTLOOPFIN
		MOV CL, 10
		DIV CL;(AL)=(AX)/10,余数在AH
		ADD AH, '0'
		DEC BX
		MOV [BX], AH
		JMP OUTLOOP
ZERO:	
		MOV DL, '0'
		MOV AH, 2
		INT 21H
		POP DX
		POP CX
		POP BX
		POP AX
		RET

OUTLOOPFIN:
		MOV DX, BX
		MOV AH, 9
		INT 21H

		POP DX
		POP CX
		POP BX
		POP AX
		RET
DECOUT ENDP

