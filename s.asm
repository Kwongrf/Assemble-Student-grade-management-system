		XOR SI, SI
		PUSH CX
ACCUMULATE:
		ADD AX, [BX+SI]
		INC SI
		INC SI	
		LOOP ACCUMULATE

		POP CX
		OR CX, CX 
		DIV CX

		PUSH AX
		MOV AX, DX
		MOV DX, 10	
		MUL DX	
		DIV CX	
		CMP AX, 5;四舍五入
		JB ACCEND	
		POP AX
		INC AX
		JMP NEXT1
		
		

ACCEND:		
		POP AX
NEXT1:
		CALL SCOREOUT
		CALL CRLF
		POP SI
		POP CX
		POP BX
		POP AX
		RET
GETAVERAGE ENDP


